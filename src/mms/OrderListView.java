    String showDetail(String order_no){
        String detail="";
        DatabaseHandler db = new DatabaseHandler();
        db.connectDatabase();
        Connection connect = db.connect;
         ResultSet rs = null;
         System.out.println("COL"+order_no);
         try {
            String query = "SELECT o.order_no,m.med_id,s.name,m.quantity,d.e_id FROM order_info o inner join order_medicine m on o.order_no = m.order_no inner join medicine s on m.med_id = s.med_id inner join deal_emp d on o.order_no=d.order_no WHERE o.order_no=?";
            PreparedStatement pStatement = connect.prepareStatement(query);
            pStatement.setString(1,order_no);

            rs=pStatement.executeQuery();
            
            while (rs.next()) {
                detail=""+rs.getInt("order_no")+"\t..."+rs.getInt("med_id")+"\t..      "+rs.getString("name")+"\t..\t"+rs.getInt("quantity")+"\t.."+rs.getInt("e_id")+"\n";
                            }

            System.out.println("Detail Query Done");
        } catch (Exception e) {
            System.out.println("Query Failed");
            e.printStackTrace();
        }
        
        
        return detail;
    }

