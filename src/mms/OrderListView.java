    public void show_orderTable(int s) {

        DatabaseHandler db = new DatabaseHandler();
        db.connectDatabase();
        ArrayList<DetailOrder> orderlist = db.orderList(s);
        DefaultTableModel model = (DefaultTableModel) orderTable.getModel();
        model.setRowCount(0);
        Object[] row = new Object[6];
        for (int i = 0; i < orderlist.size(); i++) {
            row[0] = orderlist.get(i).getOrder_no();
            row[1] = orderlist.get(i).getC_id();
            row[2] = orderlist.get(i).getOrder_time();
            row[3] = orderlist.get(i).getOrder_val();
            row[4] = orderlist.get(i).getOrder_prcsd();
            
            model.addRow(row);

        }
        for (int i = 0; i < orderTable.getRowCount(); i++) {

            orderTable.setValueAt(orderlist.get(i).getOrder_no(), i, 0);
            orderTable.setValueAt(orderlist.get(i).getC_id(), i, 1);
            orderTable.setValueAt(orderlist.get(i).getOrder_time(), i, 2);
            orderTable.setValueAt(orderlist.get(i).getOrder_val(), i, 3);
            orderTable.setValueAt(orderlist.get(i).getOrder_prcsd(), i, 4);
            orderTable.setValueAt(false, i, 5);
        }

    }

