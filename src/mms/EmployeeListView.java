    public void show_employeeTable(int s) {

        DatabaseHandler db = new DatabaseHandler();
        db.connectDatabase();
        ArrayList<Employee> emplist = db.empList(s);
        DefaultTableModel model = (DefaultTableModel) empTable.getModel();
        model.setRowCount(0);
        Object[] row = new Object[6];
        for (int i = 0; i < emplist.size(); i++) {
            row[0] = emplist.get(i).getId();
            row[1] = emplist.get(i).getName();
            row[2] = emplist.get(i).getPhone();
            row[3] = emplist.get(i).getAddress();
            row[4] = emplist.get(i).getUser_name();
            row[5] = emplist.get(i).getPassword();

            model.addRow(row);

        }
        for (int i = 0; i < empTable.getRowCount(); i++) {

            empTable.setValueAt(emplist.get(i).getId(), i, 0);
            empTable.setValueAt(emplist.get(i).getName(), i, 1);
            empTable.setValueAt(emplist.get(i).getPhone(), i, 2);
            empTable.setValueAt(emplist.get(i).getAddress(), i, 3);
            empTable.setValueAt(emplist.get(i).getUser_name(), i, 4);
            empTable.setValueAt(emplist.get(i).getPassword(), i, 5);
            empTable.setValueAt(false, i, 6);
        }

    }

