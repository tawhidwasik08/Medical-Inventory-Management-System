    void updateEmp(String eId, String name, String phone, String address, String uName, String pass) {

        DatabaseHandler db = new DatabaseHandler();
        db.connectDatabase();
        Connection connect = db.connect;
        try {

            String query1 = "UPDATE employee SET e_id = ?, name = ?,phone = ?,address = ?,user_name = ?,password = ?" + "WHERE e_id = ?";
            PreparedStatement pStatement = connect.prepareStatement(query1);
            pStatement.setString(1, eId);
            pStatement.setString(2, name);
            pStatement.setString(3, phone);
            pStatement.setString(4, address);
            pStatement.setString(5, uName);
            pStatement.setString(6, pass);
            pStatement.setString(7, eId);

            pStatement.executeUpdate();
            System.out.println("Update Query Done");
        } catch (Exception e) {
            System.out.println("Update Query Failed");
            e.printStackTrace();
        }

    }


