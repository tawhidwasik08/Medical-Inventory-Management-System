    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        Boolean canDelete = false;
        Boolean select_checked = false;
        Vector<Integer> eId = new Vector<>();
        for (int i = 0; i < empTable.getRowCount(); i++) {
            select_checked = (boolean) empTable.getValueAt(i, 6);
            //

            if (select_checked == true) {
                eId.add((int) empTable.getValueAt(i, 0));
                canDelete = true;
            }

        }
        int p = JOptionPane.showConfirmDialog(null, "Are you sure to delete these data?", "Delete", JOptionPane.YES_NO_OPTION);
        if (p == 0 && canDelete == true) {
            deleteEmp(eId);
        } else {
            JOptionPane.showMessageDialog(null, "No data to delete");
        }
        show_employeeTable(0);

    }//GEN-LAST:event_deleteBtnActionPerformed

    void deleteEmp(Vector<Integer> medId) {

        DatabaseHandler db = new DatabaseHandler();
        db.connectDatabase();
        Connection connect = db.connect;
        ResultSet rs = null;

        try {

            for (int i = 0; i < medId.size(); i++) {
                String query = "DELETE FROM employee WHERE e_id =?";
                PreparedStatement pStatement = connect.prepareStatement(query);
                pStatement.setInt(1, medId.get(i));

                pStatement.executeUpdate();
            }
            System.out.println("DELETE Employee Query Done");

        } catch (Exception e) {
            System.out.println("DELETE Employee Query Failed");
            e.printStackTrace();
        }

    }


