    public void show_stockTable(int s) {

        DatabaseHandler db = new DatabaseHandler();
        db.connectDatabase();
        ArrayList<Stock> list = db.stockList(s);
        DefaultTableModel model = (DefaultTableModel) stockTable.getModel();
        model.setRowCount(0);
        Object[] row = new Object[6];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getMed_name();
            row[2] = list.get(i).getPrice();
            row[3] = list.get(i).getQty();
            row[4] = list.get(i).getManf_name();
            row[5] = list.get(i).getExp_date();

            model.addRow(row);

        }
        for (int i = 0; i < stockTable.getRowCount(); i++) {

            stockTable.setValueAt(list.get(i).getId(), i, 0);
            stockTable.setValueAt(list.get(i).getMed_name(), i, 1);
            stockTable.setValueAt(list.get(i).getPrice(), i, 2);
            stockTable.setValueAt(list.get(i).getQty(), i, 3);
            stockTable.setValueAt(list.get(i).getManf_name(), i, 4);
            stockTable.setValueAt(list.get(i).getExp_date(), i, 5);
            stockTable.setValueAt(false, i, 6);
        }

    }

