    void updateMed(String medId, String medName, String medPrice, String medQty, String manfName, String expDate) {

        DatabaseHandler db = new DatabaseHandler();
        db.connectDatabase();
        Connection connect = db.connect;
        try {

            String query1 = "UPDATE medicine SET med_id = ?, name = ?,price = ?,avl_quantity = ?" + "WHERE med_id = ?";
            PreparedStatement pStatement = connect.prepareStatement(query1);
            pStatement.setString(1, medId);
            pStatement.setString(2, medName);
            pStatement.setString(3, medPrice);
            pStatement.setString(4, medQty);
            pStatement.setString(5, medId);

            pStatement.executeUpdate();

            String query2 = "UPDATE stock SET med_id = ?, manf_name = ?,exp_date=?" + "WHERE med_id = ?";
            PreparedStatement p2Statement = connect.prepareStatement(query2);
            p2Statement.setString(1, medId);
            p2Statement.setString(2, manfName);
            p2Statement.setString(3, expDate);
            p2Statement.setString(4, medId);

            p2Statement.executeUpdate();
            System.out.println("Update Query Done");
        } catch (Exception e) {
            System.out.println("Update Query Failed");
            e.printStackTrace();
        }

    }


